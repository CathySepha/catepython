#arithmetic operators perform simple arithmetic operations
num1 = 10
num2 = 40

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2) # modulus- returns the remainder


#Comparison Operators
print(num1 < num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 >= num2)
print(num1 == num2) # equal to
print(num1 != num2) #not equal to



#Assignment Operators
number = 12
print(number)

number += 2

print(number)

b = 56
b -= 1
print(b)

#logic operators - and, or, not
print( 34 < 10 and 89 >5 )
print( 34 < 10 or 89 >5 )
print(not (34 < 10 or 89 >5 ) )






